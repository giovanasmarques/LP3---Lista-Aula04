package ExerciciosBasicos;

public class Calculadora {
	
	public int Somar(int num1, int num2) {
		
		return num1 + num2;
	}
	public int Subtrai(int num1, int num2) {
		
		return num1 - num2;
	}
	public double Dividir(int num1, int num2) {
		
		return num1 / num2;
	}
	public int Multiplicar(int num1, int num2) {
		
		return num1 * num2;
	}
	
}
